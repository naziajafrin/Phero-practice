var color = 'red';
switch (color){
  case 'blue':
    console.log('blue')
    break;
  case 'red':
    console.log('red')
    break;
  case 'yellow':
    console.log('yellow')
    break;
  case 'green':
    console.log('green')
    break;
  case 'white':
    console.log('white')
    break;
  default:
    console.log('black')
    
}